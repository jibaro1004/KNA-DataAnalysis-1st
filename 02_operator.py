# + : 더하기
# - : 빼기
# * : 곱하기
# / : 나누기

print(7 + 14)  # 21
print(7 - 14)  # -7 (계산 결과가 음수여도 출력 가능)
print(7 * 14)  # 98
print(7 / 14)  # 0.5

print(75 + 80 / 2)  # 115.0 (나누기 먼저 계산)
print((75 + 80) / 2)  # 77.5 (괄호로 감싸서 계산 순서 지정)

print("========================더하기 연산자 실습============================")
print("두 측정값의 합:", 70 + 85)
print("온도 변화량:", "7")
print("평균 온도:", "77.5")

# ========================================================================
print("===========================실습, 합/차/평균 계산=======================")

print("두 측정값의 합:", 7 + 77.5)
print("온도 변화량:", 77.5 - 7)
print("평균 온도:", (75 + 77.5) / 2)
